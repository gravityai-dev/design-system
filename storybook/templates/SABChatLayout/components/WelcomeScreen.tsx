interface WelcomeScreenProps {
  brandName?: string;
  brandSubtitle?: string;
  logoUrl?: string;
  suggestions?: SuggestionCard[];
  onQuestionClick?: (question: string) => void;
}

export function WelcomeScreen({
  brandName = "SAB Assistant",
  brandSubtitle = "How can I help you today?",
  logoUrl,
  suggestions = [],
  onQuestionClick,
}: WelcomeScreenProps) {
  return (
    <div className={styles.container}>
      <div className={styles.content}>
        {/* Logo */}
        <div className={styles.logoContainer}>
          {logoUrl ? (
            <img src={logoUrl} alt={brandName} className={styles.logo} />
          ) : (
            <div className={styles.logoPlaceholder}>
              <Icons.sparkles className={styles.logoIcon} />
            </div>
          )}
        </div>

        {/* Title */}
        <h1 className={styles.title}>{brandName}</h1>
        <p className={styles.subtitle}>{brandSubtitle}</p>

        {/* Suggestion cards */}
        {suggestions.length > 0 && (
          <div className={styles.suggestionsGrid}>
            {suggestions.map((item, index) => {
              const IconComponent = Icons[item.icon] || Icons.chat;
              return (
                <button key={index} className={styles.suggestionCard} onClick={() => onQuestionClick?.(item.question)}>
                  <div className={styles.suggestionIcon}>
                    <IconComponent className={styles.suggestionIconSvg} />
                  </div>
                  <h3 className={styles.suggestionTitle}>{item.title}</h3>
                  <p className={styles.suggestionQuestion}>{item.question}</p>
                </button>
              );
            })}
          </div>
        )}
      </div>
    </div>
  );
}
